using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;


public class MathOperations
{
    public int Add(int numA, int numB)
    {
        return numA + numB;
    }

    public decimal Add(decimal numA, decimal numB, decimal numC)
    {
        return numA + numB + numC;
    }

    public double Add(double numA, double numB, double numC)
    {
        return numA + numB + numC;
    }
}
